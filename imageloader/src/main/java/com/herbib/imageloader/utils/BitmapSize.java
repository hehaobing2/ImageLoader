package com.herbib.imageloader.utils;

/**
 * Bitmap Size对象
 */

public class BitmapSize {
    private int mWidth;
    private int mHeight;

    public BitmapSize(int width, int height) {
        this.mWidth = width;
        this.mHeight = height;
    }

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }
}
